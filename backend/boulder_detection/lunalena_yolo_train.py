# ✅ Wrapper to make YOLO compatible with Grad-CAM
class YoloCAMWrapper(torch.nn.Module):
    def __init__(self, model):
        super(YoloCAMWrapper, self).__init__()
        self.model = model

    def forward(self, x):
        # The output of the YOLO model is a list of tensors.
        # We need to return a scalar value for Grad-CAM to work.
        preds = self.model(x)
        if isinstance(preds, tuple):
            return preds[0].max()
        return preds.max()


# ✅ Grad-CAM helper for YOLO
def get_yolo_gradcam(image_tensor):
    target_layer = yolo_model.model.model[-2] # Use the last C2f layer
    try:
        wrapped_model = YoloCAMWrapper(yolo_model.model)
        yolo_cam = GradCAM(model=wrapped_model, target_layers=[target_layer])
        grayscale_cam = yolo_cam(input_tensor=image_tensor.clone().detach().requires_grad_(True))[0, :]
        return grayscale_cam
    except Exception as e:
        print("❌ Grad-CAM failed:", e)
        return None

