# Ask for the solar incidence angle
solar_incidence_angle_input = input("Enter the solar incidence angle in degrees, or press Enter to skip depth calculation: ")
solar_incidence_angle = float(solar_incidence_angle_input) if solar_incidence_angle_input else None

# Perform inference on an image
test_image_dir = f'{dataset.location}/test/images/'
test_image_files = os.listdir(test_image_dir)
image_path = os.path.join(test_image_dir, test_image_files[0])
