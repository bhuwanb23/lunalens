                    dark_pixels = cropped_gray_boulder < np.mean(cropped_gray_boulder) * 0.5 # Threshold below half the average intensity

                    # Estimate shadow length - a very simplified approach: find the maximum extent of dark pixels in one direction
                    # A more robust approach would involve connected components and shape analysis
                    if np.sum(dark_pixels) > 0:
                        # Find the coordinates of dark pixels relative to the bounding box
                        dark_y, dark_x = np.where(dark_pixels)
                        # Estimate shadow length as the maximum extent in either x or y direction
                        shadow_length_px = max(np.max(dark_x) - np.min(dark_x), np.max(dark_y) - np.min(dark_y))
                        shadow_length_real = shadow_length_px * scale

